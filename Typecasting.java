public class Typecasting {

    public static void main(String[] args) {

        int num1 = 10;
        double num2 = num1;
        System.out.println("num2: " + num2);

        double num3 = 15.75;
        int num4 = (int) num3;
        System.out.println("num4: " + num4);

        long num5 = 1234567890L;
        int num6 = (int) num5;
        System.out.println("num6: " + num6);

    }
}
