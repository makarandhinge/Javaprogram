public class TypeCastExample7_3 {
    public static void main(String[] args)
    {
        int a = 10;
        long b = a; // implicit type casting of int to long
        System.out.println("Value of b is: " + b);
        float f = 10.5f;
        double d = f; // implicit type casting of float to double
        System.out.println("Value of d is: " + d);

    }
    
}
