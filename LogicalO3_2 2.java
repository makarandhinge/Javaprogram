public class LogicalO3_2 {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println("Input is a is true and b is false");
        System.out.println("a && b = " + (a&&b));
        System.out.println("a || b = " + (a||b) );
        System.out.println("!(a && b) = " + !(a && b));
    }
    
}
