public class WrapperClass15_3 {
    public static void main(String[] args) {
        Integer integerValue = 100;

        // Converting Integer object to byte
        byte byteValue = integerValue.byteValue();
        System.out.println("Byte value: " + byteValue);

        // Converting Integer object to short
        short shortValue = integerValue.shortValue();
        System.out.println("Short value: " + shortValue);

        // Converting Integer object to double
        double doubleValue = integerValue.doubleValue();
        System.out.println("Double value: " + doubleValue);
    }
}
