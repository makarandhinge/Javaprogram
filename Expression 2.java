/*
 * What will be the result of the following expression:
 */


public class Expression {
    public static void main(String[] args)
    {
        float a = 7 / 4.0f * 9 / 2.0f;
        /*
         * 7 / 4 = 1.75
         * 
         * 9 / 2 = 4.5
         * 
         * 1.75 * 4.5 = 7.875
         * 
         */
        System.out.println(a);
    }
}
